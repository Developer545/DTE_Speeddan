export const getTheme = (themeName) => themes[themeName] || themes.sunset;

/**
 * Obtiene lista de todos los temas disponibles
 */
export const getThemeList = () => {
  return Object.keys(themes).map(key => ({
    key,
    name: themes[key].name,
    accent: themes[key].accent
  }));
};

/**
 * Crea un tema personalizado mezclando con uno existente
 */
export const createCustomTheme = (baseTheme = 'sunset', overrides = {}) => {
  const base = getTheme(baseTheme);
  return { ...base, ...overrides };
};
